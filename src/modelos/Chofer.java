package modelos;

/**
 *
 * @author LEONARDO
 */
public class Chofer {
    private int DNI;
    private String Nombre;

    public Chofer(int DNI, String Nombre) {
        this.DNI = DNI;
        this.Nombre = Nombre;
    }

    public int getDNI() {
        return DNI;
    }

    public void setDNI(int DNI) {
        this.DNI = DNI;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
    
    
}
